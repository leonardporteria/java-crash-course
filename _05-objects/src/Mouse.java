public class Mouse {
    String brand;
    int dpi;
    int numberOfButtons;
    boolean programmable;
    boolean rgb;

    Mouse(String brand, int dpi, int numberOfButtons, boolean programmable, boolean rgb) {
        this.brand = brand;
        this.dpi = dpi;
        this.numberOfButtons = numberOfButtons;
        this.programmable = programmable;
        this.rgb = rgb;
    }
}
